def number_to_month(month):
    if month == 1:
        return print("enero")
    elif month == 2:
        return print("febrero")
    elif month == 3:
        return print("marzo")
    elif month == 4:
        return print("abril")
    elif month == 5:
        return print("mayo")
    elif month == 6:
        return print("junio")
    elif month == 7:
        return print("julio")
    elif month == 8:
        return print("agosto")
    elif month == 9:
        return print("septiembre")
    elif month == 10:
        return print("octubre")
    elif month == 11:
        return print("noviembre")
    elif month == 12:
        return print("diciembre")
    else:
        return print("error")
